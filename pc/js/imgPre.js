this.imagePreview = function(){	
	/* CONFIG */
		
		xOffset = 10;
		yOffset = 30;
		
		// these 2 variable determine popup's distance from the cursor
		// you might want to adjust to get the right result
		
	/* END CONFIG */
	$("a.preview").hover(function(e){
		this.t = this.title;
		this.title = "";	
		var c = (this.t != "") ? "<br/>" + this.t : "";
		$("body").append("<div id='preview'><img width='300px' src='' alt='Image preview' />"+ c +"</div>");								 
		$("#preview")
			.css("top",(e.pageY - xOffset) + "px")
			.css("left",(e.pageX + yOffset) + "px")
			.fadeIn("fast");
		$("#preview").find("img").attr('src',this.rel);
						
    },
	function(){
		this.title = this.t;	
		$("#preview").remove();
    });	
	$("a.preview").mousemove(function(e){
		$("#preview")
			.css("top",(e.pageY - xOffset) + "px")
			.css("left",(e.pageX + yOffset) + "px");
	});			
};


// starting the script on page load
$(document).ready(function(){
	imagePreview();
	$('.tooltips').tooltip();
	
	//手风琴动画效果
var _index=0;
$(".recm ul li").hover(function(){
	_this=$(this);
	_index=_this.index();
	_this.stop().stop().animate({marginRight:0},500).siblings("li").stop().animate({marginRight:"-42.5px"},500);
},function(){
$(this).stop().animate({marginRight:"-42.5px"},500);
});
});